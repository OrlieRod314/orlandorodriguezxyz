						<p>This article was co authored alongside a long-time friend, Matthew Ricci. About a month into online classes, we decided to compile some advice for people who found themselves falling behind in their classes. We layed out some broad topics and then went into depth on the different aspects of being a productive student.</p>
      <p>From what I’ve seen, when people fall behind in school, it’s usually not because the classes they’re taking are too hard. It’s because they come up with excuses to not follow their schedule. These things snowball over the course of a week. Then you’ll spend all weekend getting caught up. You begin to believe that this is just the way things are because you’re overworked, and you shift all blame outwards.</p>
      <p>The school year quickly becomes a weekly game of catch-up. Before you know it, you’re typing up 2 final drafts while cramming for a midterm the next day. Believe me, it’s a miserable experience.</p>
      <h1 id="damage-control">Damage Control</h1>
      <p>Well, the worst has come to pass, you are disappointed in yourself after receiving your first midterm grades. On the bright side, your GPA is completely salvageable! But how?</p>
      <p>You’re not going to like this, but you’re going to have to give up a weekend.</p>
      <p>Ideally you’ll be playing catchup about a month into the semester at the latest.</p>
      <p>Any longer and the workload may become too much.</p>
      <p>You should go through the course materials for all your classes, and then study up on everything you failed at. It’s crucial to do a refresher now before you get overwhelmed by the rest of the course. Work a bit ahead on any assignments you may have due in the next week so you can hit the ground running.</p>
      <p>Once you’re confident that you’ve brushed up and given yourself academic first-aid, you can now start to focus on how you’re going to change things so that this never happens again. Contrary to popular belief, willpower may not be the way to go here.</p>
      <h1 id="adjusting-transaction-costs-and-options">Adjusting Transaction Costs and Options</h1>
      <p>It hurts to use willpower to do things. Willpower is tough. Yet, I’ve noticed that willpower only comes into play when I have a choice between being productive or not.</p>
      <p>When you’re in class there’s simply no choice but to pay attention (assuming there are no distractions). You don’t have much of a choice, and so it doesn’t take that much willpower to focus. But given the choice between tuning in to an online lecture or playing video games in the background, there will definitely be problems. You will fail not because you are incapable of focusing, but because you can’t bring yourself to focus when given the option not to.</p>
      <p>Willpower has the obvious transaction cost of mental pain. And it’s easy to get stressed by sheer self-discipline. Many people can simply resist the temptation to slack off at least until they’re done working, but what if you can’t?</p>
      <p>The modus operandi here is to minimize the transaction cost of being productive, and maximize the transaction cost of slacking off.</p>
      <p>Do you find yourself playing video games instead of going to lecture? Have you been eating way too much hot ramen (I know I have)? Are you disappointed by your lack of exercise while there’s equipment in the other room waiting to be used?</p>
      <p>Well, you could solve these problems in a variety of ways. You could maximize the transaction cost of playing video games by creating a separate user account strictly for non-school things and setting a super long password for it. You won’t be so inclined to slack off if it involves typing in a 16-character long password. You could also keep it turned off most of the time, or flat out buy a new computer just for school.</p>
      <p>Maybe you could maximize the transaction cost of eating ramen by forcing yourself to eat it with vegetables and adding meat. If you’re going to inevitably eat ramen, at least do yourself the favor of adding healthy foods to it. You could even store it way up high in the pantry and move the healthier foods closer. Make junk food inconvenient.</p>
      <p>Instead of forcing yourself to exercise, you could incorporate it as part of your morning routine. Lay down a mat specifically for doing stretches, push ups, and crunches every morning as soon as you get out of bed. Make it so that you cannot miss it when you wake up. Adjust your routine and environment to make productivity a part of your life.</p>
      <p>Another strategy for combating indecisiveness is to simply reduce your options. Tell yourself that you won’t play video games until a certain time, or get yourself to spend at least half an hour a day doing something. If you simply choose to make a decision for yourself, then you don’t have to worry about having to make a choice in the heat of the moment. It’s inevitable that this will lead to a reinvention of the self almost. In other words, you may have to redefine your mentality and habits in order to improve your life.</p>
      <h1 id="handling-technology">Handling Technology</h1>
      <p>I am a strong advocate against social media sites like Instagram, Snapchat, and Facebook. This is mostly because I find it hard to resist the temptation to spend hours a day browsing through them. Sure, it sounds cool being able to look through your friend’s photos and post things that you do, but it seems very shallow. Social media is just fake, and I never cared about the people on there. Also, it’s just a gigantic data collection operation. Social media is free because your information is the product.</p>
      <p>Cell phones used to just be for calling and texting, now you can pretty much live your life wired to your phone. You can pay your bills, attend work meetings, make purchases and conduct your private affairs all on the same device. I find it scary, but that’s besides the point.</p>
      <p>I recommend locking away your phone for a predetermined amount of time. Say, about five hours of no phone in exchange for five hours of work. You could be done with all your homework for the next few days if you just did that right now. But you’re not going to because you’re too busy talking with friends on Instagram.</p>
      <p>You could also install browser plugins to limit the amount of time you can spend on Netflix or YouTube.</p>
      <h1 id="utilizing-time-scarcity">Utilizing Time Scarcity</h1>
      <p>Some people choose to have a job while doing schoolwork, and the workload of school is already quite intense. Ironically though, having a job actually helps you become more productive since it restricts your free time. If you have an essay due in a week, you might wait until the last day to even start. But if your essay is due in two hours, all of a sudden you’re the hardest person working in the world. The reason is simple: the less time you have, the wiser you have to be with it, and the more urgent things become. You may think having more free time means you have more time to get stuff done. But unless you’re a productivity demon, you’re going to spend 60% of your day slacking off. This fits in with the idea of not using willpower, but the environment to your advantage. Making good things easy to do, and bad things hard to do. This is similar to that in that putting a fire under your ass by choking off your total amount of time can help you get motivated. So getting a job will not only be a good source of side money, but it will also force you to be more productive.</p>
      <p>Do you usually take an entire afternoon to do your homework? Try giving yourself an hour to do as much as possible. You’ll be surprised by how much you can get done with an artificial deadline. Do this in small increments each day, and you’ll find yourself spending only a couple of hours a day on homework/studying. Isn’t this so much better than giving up entire weekends to barely get caught up? Heck, you could even add an extra half hour a day to get ahead in the course. This way you can build up a grade cushion in case you fall behind a bit.</p>
      <h1 id="developing-your-heuristic">Developing Your Heuristic</h1>
      <p>Alright, so now you know some of the general principles and ideas behind productivity. You also have digested the fact that willpower alone is not enough to be productive. Okay, so what now?</p>
      <p>Well, in order to minimize decision making and maximize efficiency, you will have to come up with a <a href="https://en.wikipedia.org/wiki/Heuristic">heuristic</a></p>
<pre>
heuristic:
        
heu·​ris·​tic | \ hyu̇-ˈri-stik
         
     : involving or serving as an aid to learning, discovery, or problem-solving 
       by experimental and especially trial-and-error methods 

also : of or relating to exploratory problem-solving techniques that utilize 
       self-educating techniques (such as the evaluation of feedback) to improve 
       performance 
</pre>
      <p>The key is practicality. For example, if you find yourself conflicted on whether or not to buy something, you should evaluate the objective pros and cons of doing so. Less money in your bank account, more satisfaction, perhaps it saves you time? Then attach the respective weights to said pros and cons, and then you’ll have your decision.</p>
      <p>An academic implementation of this is:</p>
<pre>
				I want to play video games  
					   |
					   V
			    	   Is it 5PM yet?
			|                               |
			V                               V
